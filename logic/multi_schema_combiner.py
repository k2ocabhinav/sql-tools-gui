def combine_text_for_schemas(
    raw_sql: str,
    selected_schemas: list,
    output_path: str,
    source_name: str = "Pasted SQL",
) -> dict:
    """
    Build a multi-schema output file using one pasted SQL source block.

    Returns:
        dict with keys:
            'output_file'     : str  — absolute path of the generated file
            'schemas'         : list — schemas used
            'files_processed' : int  — number of source blocks combined (always 1)
            'errors'          : list — validation or processing errors
    """
    results = {
        'output_file': None,
        'schemas': selected_schemas,
        'files_processed': 0,
        'errors': [],
    }

    if not selected_schemas:
        results['errors'].append("No schemas selected.")
        return results

    content = raw_sql.strip()
    if not content:
        results['errors'].append("No SQL text provided.")
        return results

    source_items = [(source_name, content)]
    combined_sql = _build_combined_sql(source_items, selected_schemas)

    output_path = Path(output_path)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, 'w', encoding='utf-8', newline='\n') as f:
        f.write(combined_sql)

    results['output_file'] = str(output_path)
    results['files_processed'] = 1
    return results


